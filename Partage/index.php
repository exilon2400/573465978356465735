				<p>Des opportunistes parmi tant d’autres ont commencés à faire des affaires avec les russes. La Fédération Russe a secrètement construit une usine de fabrication d’arme. En effet, son objectif est de soulever une nouvelle révolution afin que l’île soit à nouveau engagée dans un conflit armé. De ce fait, les russes pourront saisir l’occasion d’être les nouveaux “sauveurs” de Malden, et ainsi préserver la totalité du territoire pour leur propre usage. Cependant, leur route vers la révolution n’est pas toute tracée. Leur usine de fabrication d’arme, nécessite certains composants et matériaux, qui sont seulement disponible sur l’île principale. L’armée Russe ne pouvant y aller par ses propres moyens, est obligée d’engager les plus grandes mafias du pays, afin de leur apporter les ressources nécessaire. Les mafias étant pour la plupart, camouflées sous un regard civil, il est facile pour elles de se procurer un passeport, et un visa pour Malden. Néanmoins, certaines entreprises de Malden, profite également des affaires et des opportunités qu’offre les russes. En effet, le conflit ayant ravagé le territoire principal, les ressources ne sont plus d’aussi bonne qualité qu’avant. Ces ressources sont néanmoins, disponible en grande quantité, et de meilleur qualité dans les îles et îlots, sous le contrôle de la Fédération Russe. Les civils ont autorisation de s’y rendre à l’aide d’un visa exceptionnel, leur accordant le droit d’aller récolter telle ressource, à tel endroit pendant un temps défini. Certaines Mafias, profite de cette situation pour envoyer des infiltrés récolter des informations, et voler des pièces d’armes afin de s'accorder les faveurs de l’OTAN.</p>
